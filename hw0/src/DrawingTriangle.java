public class DrawingTriangle {
    public static void main(String[] args) {
        // Draw Trangles
        for (int m = 1; m <= 5; m++) {
            for (int n = 1; n <=m; n++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
